	<article>
		<h2>3. Rejestracja i Konto Użytkownika</h2>
		<p>
			Rejestracja w Aplikacji jest możliwa tylko przez Administratora, który dodaje Użytkowników do
			panelu administracyjnego.
		</p>
		<p>
			Użytkownik może zalogować się do Aplikacji poprzez podanie adresu e-mail, na który zostanie
			wysłany 4-cyfrowy kod logowania.
		</p>
		<p>
			Użytkownik jest odpowiedzialny za zapewnienie, że jego dane kontaktowe są aktualne i poprawne.
		</p>
		<p>
			Administrator zastrzega sobie prawo do zawieszenia lub usunięcia konta Użytkownika w przypadku
			naruszenia Zasad.
		</p>
	</article>

	<br />

	<article>
		<h2>4. Korzystanie z Aplikacji</h2>
		<p>
			Użytkownik zobowiązuje się do korzystania z Aplikacji zgodnie z obowiązującymi przepisami
			prawa oraz postanowieniami niniejszych Zasad.
		</p>
		<p>
			Zabronione jest dostarczanie treści o charakterze bezprawnym, w tym treści naruszających prawa
			autorskie, prawa osobiste oraz inne prawa osób trzecich.
		</p>
		<p>
			Użytkownik zobowiązuje się do niepodejmowania działań, które mogą zakłócać funkcjonowanie
			Aplikacji.
		</p>
	</article>

	<br />

	<article>
		<h2>5. Role i Uprawnienia Użytkowników</h2>
		<p><strong>Klient</strong>: Może przeglądać ofertę produktów i składać zamówienia.</p>
		<p>
			<strong>Doradca</strong>: Może przeglądać ofertę produktów, składać zamówienia w imieniu
			Klientów oraz komunikować się z Klientami.
		</p>
		<p>
			<strong>Administrator</strong>: Ma dostęp do wszystkich funkcji Aplikacji, w tym zarządzania
			użytkownikami, produktami i zamówieniami.
		</p>
		<p><strong>Zablokowany</strong>: Nie ma dostępu do Aplikacji.</p>
	</article>

	<br />

	<article>
		<h2>6. Zamówienia i Płatności</h2>
		<p>Aplikacja umożliwia składanie zamówień na produkty oferowane przez ALDO.</p>
		<p>
			Warunki realizacji zamówień, w tym ceny produktów, terminy dostawy i koszty wysyłki, określane
			są na stronach Aplikacji.
		</p>
		<p>
			Płatności za zamówienia dokonywane są za pośrednictwem dostępnych w Aplikacji metod płatności.
		</p>
		<p>
			Administrator zastrzega sobie prawo do odmowy realizacji zamówienia w przypadku braku
			możliwości jego realizacji lub podania przez Użytkownika nieprawdziwych danych.
		</p>
	</article>

	<br />

	<article>
		<h2>7. Odpowiedzialność</h2>
		<p>
			Administrator nie ponosi odpowiedzialności za czasowe niedostępności Aplikacji spowodowane
			awariami technicznymi, konserwacją lub czynnikami niezależnymi od Administratora.
		</p>
		<p>
			Administrator nie ponosi odpowiedzialności za szkody powstałe w wyniku korzystania z Aplikacji
			przez Użytkownika w sposób niezgodny z Zasadami lub przepisami prawa.
		</p>
	</article>

