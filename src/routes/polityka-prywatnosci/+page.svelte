	<article>
		<h2>4. Przetwarzanie danych osobowych</h2>
		<ul class="list-disc list-inside">
			<li>
				<strong>Podstawa prawna przetwarzania</strong>: Dane osobowe są przetwarzane zgodnie z RODO
				(Rozporządzenie Parlamentu Europejskiego i Rady (UE) 2016/679) oraz innymi obowiązującymi
				przepisami prawa.
			</li>
			<li>
				<strong>Zgoda</strong>: Przetwarzanie danych osobowych odbywa się na podstawie zgody
				użytkownika, która może być w każdej chwili wycofana.
			</li>
			<li>
				<strong>Konieczność przetwarzania</strong>: Przetwarzanie danych jest niezbędne do wykonania
				umowy zawartej z użytkownikiem oraz realizacji usług oferowanych przez Twoje ALDO.
			</li>
		</ul>
	</article>

	<br />

	<article>
		<h2>5. Przechowywanie i ochrona danych</h2>
		<ul class="list-disc list-inside">
			<li>
				<strong>Zabezpieczenia</strong>: Dane osobowe są przechowywane na zabezpieczonych serwerach
				i chronione zgodnie z obowiązującymi przepisami prawa oraz najlepszymi praktykami w zakresie
				ochrony danych.
			</li>
			<li>
				<strong>Okres przechowywania</strong>: Dane osobowe są przechowywane przez okres niezbędny
				do realizacji celów, dla których zostały zebrane, a następnie usuwane zgodnie z
				obowiązującymi przepisami prawa.
			</li>
		</ul>
	</article>

